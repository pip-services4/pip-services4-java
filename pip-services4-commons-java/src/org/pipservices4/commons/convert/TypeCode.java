package org.pipservices4.commons.convert;

/**
 * Codes for the data types that can be
 * converted using {@link TypeConverter}.
 *
 * @see TypeConverter
 */
public enum TypeCode {
    Unknown,
    String,
    Boolean,
    Integer,
    Long,
    Float,
    Double,
    DateTime,
    Duration,
    Object,
    Enum,
    Array,
    Map,
}
